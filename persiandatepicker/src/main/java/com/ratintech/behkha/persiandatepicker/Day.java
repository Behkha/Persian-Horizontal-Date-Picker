package model;

public class Day {

    private int num;
    private String day;

    public Day(int num, String day) {
        this.num = num;
        this.day = day;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }
}
